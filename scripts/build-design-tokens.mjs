#!/usr/bin/env node
/**
 * Reads design-system/tokens.json and writes app/design-tokens.css
 * and app/ds-tailwind-theme.css (Tailwind @theme colors: only --ds-* from tokens).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const src = path.join(root, "design-system", "tokens.json");
const out = path.join(root, "app", "design-tokens.css");
const themeOut = path.join(root, "app", "ds-tailwind-theme.css");

function colorToCss(value) {
  if (!value || typeof value !== "object") return null;
  if (value.hex != null && value.alpha === 1) return value.hex;
  const c = value.components;
  if (!Array.isArray(c) || c.length < 3) return value.hex ?? null;
  const r = Math.round(c[0] * 255);
  const g = Math.round(c[1] * 255);
  const b = Math.round(c[2] * 255);
  const a = value.alpha ?? 1;
  if (a === 1) return `rgb(${r} ${g} ${b})`;
  return `rgb(${r} ${g} ${b} / ${a})`;
}

/** Figma uses `%` in names; `%` is not valid in CSS custom property identifiers. */
function slugSegment(seg) {
  return String(seg).replace(/%/g, "pct");
}

function walkPrimitivesColor(node, prefix, acc) {
  if (!node || typeof node !== "object") return;
  if (node.$type === "color" && node.$value) {
    const css = colorToCss(node.$value);
    if (css) acc[prefix.join("-")] = css;
    return;
  }
  for (const [k, v] of Object.entries(node)) {
    if (k.startsWith("$")) continue;
    walkPrimitivesColor(v, [...prefix, slugSegment(k)], acc);
  }
}

function walkNumbers(node, prefix, acc) {
  if (!node || typeof node !== "object") return;
  if (node.$type === "number" && typeof node.$value === "number") {
    acc[prefix.join("-")] = `${node.$value}px`;
    return;
  }
  for (const [k, v] of Object.entries(node)) {
    if (k.startsWith("$")) continue;
    walkNumbers(v, [...prefix, slugSegment(k)], acc);
  }
}

function collectSemanticColors(node, acc, stack) {
  if (!node || typeof node !== "object") return;
  if (node.$type === "color" && node.$value) {
    const key = stack.at(-1);
    if (!key) return;
    const css = colorToCss(node.$value);
    if (!css) return;
    if (acc.has(key)) {
      throw new Error(`Duplicate semantic color key "${key}"`);
    }
    acc.set(key, css);
    return;
  }
  for (const [k, v] of Object.entries(node)) {
    if (k.startsWith("$")) continue;
    stack.push(k);
    collectSemanticColors(v, acc, stack);
    stack.pop();
  }
}

function linesFromMap(map, indent = "  ") {
  return [...map.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([k, v]) => `${indent}--ds-${k}: ${v};`)
    .join("\n");
}

const raw = JSON.parse(fs.readFileSync(src, "utf8"));

const primitives = raw.primitives?.mobile;
if (!primitives) {
  console.error("tokens.json: missing primitives.mobile");
  process.exit(1);
}

const primitiveColors = {};
walkPrimitivesColor(primitives.color, [], primitiveColors);

const radius = {};
const spacing = {};
const strokeWidths = {};
walkNumbers(primitives.utilities?.radius ?? {}, [], radius);
walkNumbers(primitives.utilities?.spacing ?? {}, [], spacing);
walkNumbers(primitives.utilities?.stroke ?? {}, [], strokeWidths);

const typoSize = {};
walkNumbers(primitives.typography?.size ?? {}, ["typography", "size"], typoSize);
const typoLh = {};
walkNumbers(
  primitives.typography?.["line-height"] ?? {},
  ["typography", "line-height"],
  typoLh
);

const desktop = raw.primitives?.desktop;
const desktopTypoSize = {};
walkNumbers(desktop?.typography?.size ?? {}, ["typography", "size"], desktopTypoSize);
const desktopTypoLh = {};
walkNumbers(
  desktop?.typography?.["line-height"] ?? {},
  ["typography", "line-height"],
  desktopTypoLh
);

const darkColor = new Map();
collectSemanticColors(raw.semantic?.Dark?.Color, darkColor, []);

const darkState = new Map();
collectSemanticColors(
  raw.semantic?.Dark?.["interactive state"],
  darkState,
  []
);

const lightColor = new Map();
collectSemanticColors(raw.semantic?.Light?.Color, lightColor, []);

const lightState = new Map();
collectSemanticColors(
  raw.semantic?.Light?.["interactive state"],
  lightState,
  []
);

const darkSemantic = new Map([...darkColor, ...darkState]);
const lightSemantic = new Map([...lightColor, ...lightState]);

const header = `/* Generated by scripts/build-design-tokens.mjs — do not edit by hand */\n`;

const primitiveBlock = Object.keys(primitiveColors)
  .sort()
  .map((k) => `  --ds-${k}: ${primitiveColors[k]};`)
  .join("\n");

const radiusBlock = Object.keys(radius)
  .sort()
  .map((k) => `  --ds-${k}: ${radius[k]};`)
  .join("\n");

const spacingBlock = Object.keys(spacing)
  .sort()
  .map((k) => `  --ds-${k}: ${spacing[k]};`)
  .join("\n");

const strokeBlock = Object.keys(strokeWidths)
  .sort()
  .map((k) => `  --ds-${k}: ${strokeWidths[k]};`)
  .join("\n");

const typoSizeBlock = Object.keys(typoSize)
  .sort()
  .map((k) => `  --ds-${k}: ${typoSize[k]};`)
  .join("\n");

const typoLhBlock = Object.keys(typoLh)
  .sort()
  .map((k) => `  --ds-${k}: ${typoLh[k]};`)
  .join("\n");

const desktopTypoSizeBlock = Object.keys(desktopTypoSize)
  .sort()
  .map((k) => `  --ds-${k}: ${desktopTypoSize[k]};`)
  .join("\n");

const desktopTypoLhBlock = Object.keys(desktopTypoLh)
  .sort()
  .map((k) => `  --ds-${k}: ${desktopTypoLh[k]};`)
  .join("\n");

const css = `${header}
/* Primitive palette, radius, spacing, stroke, typography (Figma mobile / _Primitives) */
html {
${primitiveBlock}

${radiusBlock}

${spacingBlock}

${strokeBlock}

${typoSizeBlock}

${typoLhBlock}
}

/* Typography scale at sm+ uses desktop primitives */
@media (min-width: 40rem) {
  html {
${desktopTypoSizeBlock}

${desktopTypoLhBlock}
  }
}

/* Semantic tokens: Dark (default — matches current app) */
html:not([data-theme="light"]) {
${linesFromMap(darkSemantic)}
}

/* Semantic tokens: Light */
html[data-theme="light"] {
${linesFromMap(lightSemantic)}
}
`;

fs.writeFileSync(out, css);
console.log("Wrote", path.relative(root, out));

const tailwindColorKeys = new Set([
  ...Object.keys(primitiveColors),
  ...darkSemantic.keys(),
]);
const themeBody = [...tailwindColorKeys]
  .sort((a, b) => a.localeCompare(b))
  .map((k) => `  --color-ds-${k}: var(--ds-${k});`)
  .join("\n");

